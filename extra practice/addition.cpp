#include<iostream>
using namespace std;

int main() {
    int x = 20;
    int y = 32;
    int sum = x + y;//command for adding two numbers
    cout << x;
    cout <<" + ";
    cout << y;
    cout <<" = ";
    cout << sum;//sum of the two number will be shown
    cout <<"\n\n";
    cout << "sum of "<< x <<" and "<< y <<" is "<< sum;//statement type
    cout << "\n\n";
    cout << x + y <<"\n";//direct sum command
    return 0;
}
